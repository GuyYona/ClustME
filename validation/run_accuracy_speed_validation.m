% run_accuracy_speed_validation - Validation suite for Static-V approximation
%
%   This script benchmarks the mathematical fidelity and computational efficiency 
%   of the Static-V algorithm against a full iterative Linear Mixed-Effects (LME) 
%   refit. This is a scientific validation script designed to generate empirical 
%   evidence for the manuscript, distinct from routine software unit tests.
%
% Syntax:
%   run_accuracy_speed_validation()
%
% Validation Targets:
%   1. Fidelity: Evaluates the correlation (R^2), slope, and Mean Absolute Deviation 
%      (MAD) between t-statistics generated by Static-V and the full LME over a 
%      batch of simulated datasets.
%   2. Efficiency: Extrapolates the execution time of full LME refitting to 
%      quantify the multiplicative speedup factor achieved by Static-V.
%
% Outputs:
%   - Terminal: Summary statistics (R^2, Slope, Intercept, MAD) and speedup factor.
%   - Figures: Figure 2 (Panel A: Identity, Panel B: Fidelity Metrics, Panel C: Speedup).
%
% Dependencies:
%   - MATLAB R2019b or newer.
%   - validation_settings.json (for export directory configuration).
%   - Part of the ClustME Validation Suite.
%
% Reference:
%   Yona, G., & Magill, P. J. Fast Cluster-Based Permutation Testing with
